txt = "Somos os chamados \"vikings\" do norte"
print(txt)

# Retira o que vem antes do \r
txt2 = "Olá\rMundo"
print(txt2)

# \t representa TAB
txt3 = "Olá\tMundo"
print(txt3)

# Escreve uma barra invertida  >> \\ = \
txt4 = "teste \\be"
print(txt4)

txt5 = 'It\'s Alright'
print(txt5)

# Deleta = backspace
txt6 = "Olá \bMundo"
print(txt6)

# Uma barra invertida seguida por um 'x' e um número hexadecimal representa um valor hexadecimal:
txt7 = "\x48\x65\x6c\x6c\x6f"
print(txt7) 
# Uma barra invertida seguida por três inteiros resultará em um valor octal:
txt8 = "\110\145\154\154\157"
print(txt8) 